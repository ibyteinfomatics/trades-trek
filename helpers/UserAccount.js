
// account value ............................
export const AccountValue=(cash,invested,profit)=>{
  
    return (cash+invested+profit).toFixed(2)
}

// Buying Power ................................
export const BuyingPower=(acc)=>{
    return acc.toFixed(2)

}

// user cash .................................
export const UserCash=(acc)=>{
    return acc.toFixed(2)

}

